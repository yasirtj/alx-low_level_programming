#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first int
 * @b: second int
 * Return: a + b
 **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: first int
 * @b: second int
 * Return: a - b
 **/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: a * b
 **/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first int
 * @b: second int
 * Return: a / b
 **/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get modulo of two integers
 * @a: first int
 * @b: second int
 * Return: a % b
 **/

int op_mod(int a, int b)
{
	return (a % b);
}
